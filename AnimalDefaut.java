package Modificadores;

public class AnimalDefaut {
	 String nome;
	    String cor;
	    String tamanho;
	    String peso;
	    String raca;

	    String getNome() {
	        return nome;
	    }

	    void setNome(String nome) {
	        this.nome = nome;
	    }

	    String getCor() {
	        return cor;
	    }

	    void setCor(String cor) {
	        this.cor = cor;
	    }

	    String getTamanho() {
	        return tamanho;
	    }

	    void setTamanho(String tamanho) {
	        this.tamanho = tamanho;
	    }

	    String getPeso() {
	        return peso;
	    }

	    void setPeso(String peso) {
	        this.peso = peso;
	    }

	    String getRaca() {
	        return raca;
	    }

	    void setRaca(String raca) {
	        this.raca = raca;
	    }
	}